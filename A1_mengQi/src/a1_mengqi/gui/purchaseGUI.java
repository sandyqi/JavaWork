    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jList2.isFocusable()){
            int[] quantitiesForOrder = new int[quantities.size()];
            // how to translate a List<Integer> to a int[] ?  Only for Integer[]?
            for(int i =0; i < quantitiesForOrder.length;i++){
                quantitiesForOrder[i] = quantities.get(i);
            }
            sandy.placeOrder(String.valueOf(cid), productIds.toArray(new String[productIds.size()]),quantitiesForOrder);
            System.out.println("Placed order successfully");
            JOptionPane.showMessageDialog(null, "Order is placed successfully");
            
        }else{
             JOptionPane.showMessageDialog(null, "Please Select One Product");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

