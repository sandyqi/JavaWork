    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jList1.isSelectionEmpty() || (jList1.getSelectedIndex()==0)){
            JOptionPane.showMessageDialog(null, "Please Select One Product");
        }else if(Integer.parseInt(jTextField1.getText())<=0){
            System.out.println(Integer.parseInt(jTextField1.getText()));
            JOptionPane.showMessageDialog(null, "Please reset quantities");
        }else{
            forJList2.add(jList1.getSelectedValue().toString()+"   "+jTextField1.getText());// get the string that will display in jList2
            productIds.add(jList1.getSelectedValue().toString().substring(0, 4)); // add the productId from the string of jTextField1
            quantities.add(Integer.parseInt(jTextField1.getText())); // add the quantity
            jList2.setListData(forJList2.toArray()); // update jList2
            jList2.setFocusable(true);
          //  forJList2.add(null)
          
        }
    }//GEN-LAST:event_jButton1ActionPerformed

